private void showEmpData()
{
    try
    {
        ArrayList<DoctorPojo> doctorList=DoctorDao.getAllDoctors();
        Object []rows=new Object[5];
        DefaultTableModel model=(DefaultTableModel)jtEmpData.getModel();
        for(DoctorPojo doc:doctorList)
        {
            rows[0]=doc.getUserid();
            rows[1]=doc.getDoctorid();
            rows[2]=doc.getQualification();
            rows[3]=doc.getSpecialist();
            rows[4]=doc.getIsActive();
            model.addRow(rows);
        }
    }
     catch(Exception sq)
     {
           JOptionPane.showMessageDialog(null, "Error while connecting to DB!","Exception!",JOptionPane.ERROR_MESSAGE); 
           sq.printStackTrace();
     } 
}
