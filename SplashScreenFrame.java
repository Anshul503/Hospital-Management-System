 class SplashThread extends Thread
  {
      @Override
      public void run()
      {
        int count=1;
        Random  r=new Random();
        while(jProgressBar1.getValue()<jProgressBar1.getMaximum())
        {
          try
         {
            jProgressBar1.setValue((int)count); 
            Thread.sleep(1200);
            count=count+r.nextInt(100);
          
         }
         catch(InterruptedException ex)
         {
             JOptionPane.showMessageDialog(null,"Exception in  Thread","Error!"+ex.getMessage(),JOptionPane.ERROR_MESSAGE);
         }
        }
        dispose();
        LoginFrame loginFrame=new LoginFrame();
        loginFrame.setVisible(true);
      }
  
     }

